import { MetadataRoute } from 'next'

export default function robots(): MetadataRoute.Robots {
    return {
        rules: {
            userAgent: '*',
            allow: '/',
            disallow: ['/admin', '/api/auth'],
        },
        sitemap: `${process.env.NEXT_PUBLIC_APP_URL || 'https://redestampacion.com'}/sitemap.xml`,
    }
}
